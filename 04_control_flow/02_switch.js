const month = "april"

switch (month) {
    case "Jan":   
        console.log("January");
        break;
    case "feb":   
        console.log("Feb");
        break;
    case "march":   
        console.log("March");
        break;
    case "april":   
        console.log("April");
        break;
    default:
        console.log("Default case match");
        break;
}